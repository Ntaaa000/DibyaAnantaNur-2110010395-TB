    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        
         id = txt_id.getText();
    no = txt_no_aset.getText();
    model = txt_model.getText();
    merk = txt_merk.getText();
    jumlah = txt_jumlah.getText();
    kondisi = txt_kondisi.getText();

    try {
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date tanggalDate = new Date();  // Default to current date
        try {
            tanggalDate = dateFormat.parse(txt_tanggal.getText());
        } catch (ParseException e) {
            e.printStackTrace();
        }

        String sql = "UPDATE total_aset SET no_aset=?, model=?, merk=?, jumlah=?, tanggal=?, kondisi=? WHERE id=?";
        PreparedStatement preparedStatement = con.prepareStatement(sql);
        preparedStatement.setString(1, no);
        preparedStatement.setString(2, model);
        preparedStatement.setString(3, merk);
        preparedStatement.setString(4, jumlah);
        preparedStatement.setDate(5, new java.sql.Date(tanggalDate.getTime())); // Set date using java.sql.Date
        preparedStatement.setString(6, kondisi);
        preparedStatement.setString(7, id);

        preparedStatement.executeUpdate(); // Use executeUpdate for UPDATE statement
        kosongkan();
        TampilData(sql);
        JOptionPane.showMessageDialog(null, "Data Berhasil di Edit");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Data Gagal Edit \n" + e.getMessage());
    }
    }//GEN-LAST:event_editActionPerformed

