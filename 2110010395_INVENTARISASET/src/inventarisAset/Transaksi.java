private void TampilData (String sql){
    DefaultTableModel t = new DefaultTableModel();
    
    t.addColumn("ID");
    t.addColumn("No ASET");
    t.addColumn("MODEL");
    t.addColumn("MERK");
    t.addColumn("JUMLAH");
    t.addColumn("TANGGAL");
    t.addColumn("KONDISI");
    Table.setModel(t);
    try {
        int i = 1;
        stat=con.createStatement();
        res=stat.executeQuery("select * from total_aset");
        while (res.next()) {
            t.addRow(new Object[]{
            res.getString("id"),
            res.getString("no_aset"),
            res.getString("model"),
            res.getString("merk"),
            res.getString("jumlah"),
            res.getString("tanggal"),
            res.getString("kondisi"),
            });
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
    }
}
    
